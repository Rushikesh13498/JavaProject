package MockPapersMarch;

public class Q26 {


	    public static void main(String[] args) {
	        // For loop
	        System.out.println("Using for loop:");
	        for (int i = 1; i <= 5; i++) {
	            System.out.println(i);
	        }

	        // Do-while loop
	        System.out.println("\nUsing do-while loop:");
	        int j = 1;
	        do {
	            System.out.println(j);
	            j++;
	        } while (j <= 5);
	    }
	}


